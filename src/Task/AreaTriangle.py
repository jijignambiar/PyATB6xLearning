# Python program to calculate the area of a triangle using the formula

# Area = 1/2 *base*height
# base =10, height =5

base = float(input("enter base value of a triangle:"))
height = float(input("enter height of a triangle :"))
area = (1/2 * base * height)
print("Base of the triangle:",base)
print("Height of the triangle:",height)
print("Area of the triangle:",area)